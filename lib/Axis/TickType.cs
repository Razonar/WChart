using System;
using System.Collections.Generic;
using System.Text;

namespace WChart
{

	/// <summary>
	/// An enumeration of the different types of tick mark. 
	/// </summary>
	public enum TickType
	{

		/// <summary>
		/// No tick
		/// </summary>
		None = 0,

		/// <summary>
		/// A small (short physical extent) tick
		/// </summary>
		Small = 1,

		/// <summary>
		/// A large (major) tick.
		/// </summary>
		Large = 2
	}

}
